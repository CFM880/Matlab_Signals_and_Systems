nf=1:3;
Nf=length(nf);
f=[1,2,1];
nh=2:4;
Nh=length(nh);
h=[1,2,3];
left=nf(1)+nh(1);
right=nf(Nf)+nh(Nh);
y=conv(f,h);
subplot(3,1,1);
stem(nf,f,'filled');
axis([0 8 0 9]);
grid;
subplot(3,1,2);
stem(nh,f,'filled');
axis([0 8 0 9]);
grid;
subplot(3,1,3);
stem(left:right,y,'filled');
axis([0 8 0 9]);
grid;